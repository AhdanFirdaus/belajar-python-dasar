# Belajar Strings Methods
# https://docs.python.org/3/library/stdtypes.html#string-methods

nama = "Muhammad Ahdan Firdaus"
print(nama.upper())
print(nama.lower())
print(nama.title())
print(nama.swapcase())
print(nama.capitalize())
print(nama.startswith("Muhammad"))
print(nama.endswith("Firdaus"))
print(nama.find("M"))
print(nama.replace("Firdaus", "dadan"))
print(nama.split(" "))
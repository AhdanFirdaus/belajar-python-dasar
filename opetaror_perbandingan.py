# Operator Perbandingan

# > lebih dari
# < kurang dari
# >= lebih dari sama dengan
# <= kurang dari sama dengan
# == sama dengan
# != tidak sama dengan

a = 10
b = 20

print(a > b)
print(a < b)
print(a >= b)
print(a <= b)
print(a == b)
print(a != b)

# perbandingan strings
print("=======Perbandingan Strings=======")
print("Ahdan" == "Ahdan")
print("Ahdan" != "Ahdan")